public class loopsBasics {
    public static void main(String[] args) {
        int count = 2;
        while(count < 20){
            System.out.println(count);
            count++;
        }
        // for loop
        int i = 10;
        for(i = 10; i<=25 ; i++){
            System.out.println(i);
        }

    }
}

